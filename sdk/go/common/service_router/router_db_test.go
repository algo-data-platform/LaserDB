func TestPickServers(t *testing.T) {
	serverList := ServerList{
		Server{
			Host:        "192.168.1.3",
			Port:        8888,
			ServiceName: "test",
			UpdateTime:  uint64(time.Now().Add(time.Minute*-10).UnixNano() / 1e6),
			Status:      ServerStatus_AVAILABLE,
		},
		Server{
			Host:        "192.168.1.1",
			Port:        8888,
			ServiceName: "test",
			UpdateTime:  uint64(time.Now().UnixNano() / 1e6),
			Status:      ServerStatus_AVAILABLE,
		},
		Server{
			Host:        "192.168.1.2",
			Port:        9999,
			ServiceName: "test",
			UpdateTime:  uint64(time.Now().UnixNano() / 1e6),
			Status:      ServerStatus_UNAVAILABLE,
		},
	}
	db := GetRouterDb()
	pickedResult := db.PickServers("test", serverList)
	assert.Equal(t, 1, len(pickedResult), "error")
	assert.Equal(t, "192.168.1.1", pickedResult[0].Host, "error")
}
